// Program: Superhero Profile App
// Objective: Practice using different data types in a themed activity

public class SuperheroProfile {
    public static void main(String[] args) {
        // Declare string variables
        String heroName = "Spider-Man";
        String realName = "Peter Parker";
        String superPower = "Wall-crawling, super agility, spider sense";

        // Declare char variable for suit color initial (e.g., R for Red)
        char suitColorInitial = 'R';

        // Declare integer variable for first appearance year
        int firstAppearanceYear = 1962;

        // Declare boolean variable to check if the hero has a sidekick
        boolean hasSidekick = false;

        // Print superhero details
        System.out.println("Superhero Profile");
        System.out.println("-----------------");
        System.out.println("Hero Name: " + heroName);
        System.out.println("Real Name: " + realName);
        System.out.println("Super Powers: " + superPower);
        System.out.println("Suit Color Initial: " + suitColorInitial);
        System.out.println("First Appearance Year: " + firstAppearanceYear);
        System.out.println("Has a Sidekick? " + hasSidekick);
    }
}
